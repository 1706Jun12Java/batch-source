AFTER UPDATE ON BANK_ACCOUNT
FOR EACH ROW
DECLARE X VARCHAR2(40);
BEGIN
IF :OLD.CURRENT_BALANCE < :NEW.CURRENT_BALANCE THEN
DBMS_OUTPUT.PUT('A DEPOSIT OF $');
DBMS_OUTPUT.PUT(:NEW.CURRENT_BALANCE - :OLD.CURRENT_BALANCE);
