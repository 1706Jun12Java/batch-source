public class ReimbursementStatus {
	
	private int ur_id;
	private String ur_role;
	
	public ReimbursementStatus() {
		super();
	}
	
	public int getUr_id() {
		return ur_id;
	}
	public void setUr_id(int ur_id) {
		this.ur_id = ur_id;
	}
	public String getUr_role() {
		return ur_role;
	}
	public void setUr_role(String ur_role) {
		this.ur_role = ur_role;
	}
}
